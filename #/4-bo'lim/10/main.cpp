#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    double a,b;
    cout<<"a = "; cin>>a;
    cout<<"b = "; cin>>b;

    cout<<"a+b = "<< a + b << endl;
    cout<<"a*b = "<< a * b << endl;
    cout<<"a^2 = "<< a * a << endl;
    cout<<"b^2 = "<< b * b << endl;

    return 0;
}
